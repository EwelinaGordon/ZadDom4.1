public class Triangle {
    private int max;
    private int bokB;
    private int bokC;

    public void setMax(int max) {
        this.max = max;
    }

    public void setBokB(int bokB) {
        this.bokB = bokB;
    }

    public void setBokC(int bokC) {
        this.bokC = bokC;
    }

    public int getMax() {
        return max;
    }

    public int getBokB() {
        return bokB;
    }

    public int getBokC() {
        return bokC;
    }


}
